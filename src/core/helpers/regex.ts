// 常用的正则表达式

// 金额
export const REGEX_MONEY =  /^[0-9]+([.]{1}[0-9]{1,2})?$/;

// 邮箱
export const REGEX_EMAIL = /^(([^<>()[\]\\.,;:\s@\"]+(\.[^<>()[\]\\.,;:\s@\"]+)*)|(\".+\"))@((\[[0-9]{1,3}\.[0-9]{1,3}\.[0-9]{1,3}\.[0-9]{1,3}\])|(([a-zA-Z\-0-9]+\.)+[a-zA-Z]{2,}))$/; 

// 手机号
export const REGEX_PHONE = /^1(3\d|4[5-9]|5[0-35-9]|6[2567]|7[0-8]|8\d|9[0-35-9])\d{8}$/;  

// 短信验证码
export const REGEX_CHECK_CODE = /^\d{6}$/; 
